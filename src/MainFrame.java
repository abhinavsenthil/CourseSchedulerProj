    private void displayCourses(){
        DefaultTableModel model = (DefaultTableModel) allCoursesTable.getModel();
        ArrayList<CourseEntry> courses = CourseQueries.getAllCourses(currentSemester);
        Iterator<CourseEntry> iter = courses.iterator();

        int row = 0;
        model.setRowCount(courses.size());
        while (iter.hasNext()) {
            CourseEntry course = iter.next();
            allCoursesTable.setValueAt(course.getCourseCode(), row, 0);
            allCoursesTable.setValueAt(course.getDescription(), row, 1);
            allCoursesTable.setValueAt(course.getSeats(), row, 2);

            row++;
        }
        
    }
    
    
