    private void submitCourseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitCourseActionPerformed
        // TODO add your handling code here:
        String courseCode = (String) chooseCourseComboBox.getSelectedItem();
        StudentEntry student = (StudentEntry) chooseStudentComboBox.getSelectedItem();
        ScheduleEntry schedule = new ScheduleEntry(currentSemester);
        schedule.setStudentID(student.getStudentID());
        schedule.setCourseCode(courseCode);
        schedule.setTimestamp(new java.sql.Timestamp(new java.util.Date().getTime()));
        Integer enrolled = ScheduleQueries.getScheduledStudentCount(currentSemester, courseCode);
        Integer seats = CourseQueries.getCourseSeats(currentSemester, courseCode);
        String status = "S";
        String statusDesc = "Scheduled";
        if (enrolled >= seats){
            status = "W";
            statusDesc = "Waitlisted";
        }
        
        schedule.setStatus(status);
        
        
        try {
            ScheduleQueries.addScheduleEntry(schedule);
            courseScheduledLabel.setText("Course ("+courseCode+") "+statusDesc + " for: "+student.getFirstName());
            
        }
        catch(Exception ex) {
            courseScheduledLabel.setText("Error in Scheduling Course ("+courseCode+") for: "+student.toString());
            
        }
         
    }//GEN-LAST:event_submitCourseActionPerformed
 
    private void generateScheduleTable(){
        
        DefaultTableModel model = (DefaultTableModel) DisplayStudentTable.getModel();
        StudentEntry student = (StudentEntry) selectStudentComboBox.getSelectedItem();
        ArrayList<ScheduleEntry> schedules = ScheduleQueries.getScheduleByStudent(currentSemester, student.getStudentID());
        Iterator<ScheduleEntry> iter = schedules.iterator();

        int row = 0;
        model.setRowCount(schedules.size());
        while (iter.hasNext()) {
            ScheduleEntry schedule = iter.next();
            DisplayStudentTable.setValueAt(schedule.getCourseCode(), row, 0);
            DisplayStudentTable.setValueAt(schedule.getStatus(), row, 1);

            row++;
        
    }
    }
    
